#include "libft.h"

char		*ft_strdup(const char *s1)
{
	int		i;
	int		lenth;
	char	*p;

	lenth = 0;
	while (s1[lenth])
		lenth++;
	p = (char*)malloc(1 * (lenth + 1));
	if (p == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
